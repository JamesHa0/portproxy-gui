def tr(key, *args):
    """Translate a key. Optional args for format placeholders."""
    s = STRINGS.get(_current_lang, STRINGS["zh"]).get(key, key)
    if args:
        try:
            return s.format(*args)
        except (IndexError, KeyError):
            return s
    return s
